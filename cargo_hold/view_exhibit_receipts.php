<?php
session_start();
require_once('../db.php');

if (!isset($_SESSION['user_id'])) {
    header("Location: ../login.php");
    exit();
}
require_once '../includes/permissions.php';
require_permission($conn, 'exhibit_view');

if (!isset($_GET['exhibit_id'])) {
    die("Exhibit ID not specified.");
}

$exhibit_id = intval($_GET['exhibit_id']);

// Fetch Exhibit Details
$stmt = $conn->prepare("SELECT exhibit_ref, job_id FROM exhibits WHERE exhibit_id = ?");
$stmt->bind_param("i", $exhibit_id);
$stmt->execute();
$stmt->bind_result($exhibit_ref, $job_id);
if (!$stmt->fetch()) {
    die("Exhibit not found.");
}
$stmt->close();

// Fetch every saved receipt covering this exhibit, newest first.
$receipts = [];
$recStmt = $conn->prepare("
    SELECT er.receipt_id, er.receipt_type, er.booked_out_to, er.generated_at,
           CONCAT(u.first_name, ' ', u.last_name) AS generated_by_name
    FROM exhibit_receipt_items eri
    JOIN exhibit_receipts er ON eri.receipt_id = er.receipt_id
    LEFT JOIN users u ON er.generated_by = u.id
    WHERE eri.exhibit_id = ?
    ORDER BY er.generated_at DESC, er.receipt_id DESC
");
$recStmt->bind_param("i", $exhibit_id);
$recStmt->execute();
$recResult = $recStmt->get_result();
while ($row = $recResult->fetch_assoc()) {
    $receipts[] = $row;
}
$recStmt->close();

$typeLabels = [
    'in'     => 'Book In',
    'out'    => 'Book Out',
    'return' => 'Book Back In',
];

include('../header.php');
