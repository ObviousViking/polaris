                        <?php if ($rec['receipt_type'] === 'out'): ?>
                        Booked Out To: <?php echo htmlspecialchars($rec['booked_out_to'] ?? ''); ?>
                        <?php elseif ($rec['receipt_type'] === 'return'): ?>
